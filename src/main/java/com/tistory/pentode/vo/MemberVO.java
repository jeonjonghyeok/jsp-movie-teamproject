package com.tistory.pentode.vo;

public class MemberVO {

	private String CACT;
	private String CNAME;
	private String CSSN;
	private String CCTN;
	private String CMAIL;
	private String CPWD;
	
	
	
	public MemberVO() {
		super();
	}



	public String getCACT() {
		return CACT;
	}



	public void setCACT(String cACT) {
		CACT = cACT;
	}



	public String getCNAME() {
		return CNAME;
	}



	public void setCNAME(String cNAME) {
		CNAME = cNAME;
	}



	public String getCSSN() {
		return CSSN;
	}



	public void setCSSN(String cSSN) {
		CSSN = cSSN;
	}



	public String getCCTN() {
		return CCTN;
	}



	public void setCCTN(String cCTN) {
		CCTN = cCTN;
	}



	public String getCMAIL() {
		return CMAIL;
	}



	public void setCMAIL(String cMAIL) {
		CMAIL = cMAIL;
	}



	public String getCPWD() {
		return CPWD;
	}



	public void setCPWD(String cPWD) {
		CPWD = cPWD;
	}

}
